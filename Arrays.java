
// Topic: Arrays in Java
// Definition: Demonstrates 1D and 2D arrays in Java.
// Use: Helps in storing multiple values of the same data type.

public class Arrays {
    public static void main(String[] args) {
        // 1D Array
        int[] numbers = {1, 2, 3, 4, 5};
        System.out.println("1D Array Element: " + numbers[2]);

        // 2D Array
        int[][] matrix = {{1, 2, 3}, {4, 5, 6}};
        System.out.println("2D Array Element: " + matrix[1][2]);
    }
}