
// Topic: Variables and Data Types
// Definition: Demonstrates different data types and how variables are used in Java.
// Use: Helps to understand primitive and non-primitive data types in Java.

public class VariablesAndDataTypes {
    public static void main(String[] args) {
        int number = 10; // Integer data type
        double pi = 3.14; // Floating-point data type
        char letter = 'A'; // Character data type
        boolean isJavaFun = true; // Boolean data type

        System.out.println("Integer: " + number);
        System.out.println("Double: " + pi);
        System.out.println("Character: " + letter);
        System.out.println("Boolean: " + isJavaFun);
    }
}